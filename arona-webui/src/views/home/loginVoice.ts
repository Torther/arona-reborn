// eslint-disable-next-line import/prefer-default-export
export function playLoginVoice() {
  const index = randomInt(0, VoiceList.length);
  const fileName = VoiceList[index];
  const characterName = `JP_${fileName.split("_")[0]}`;
  const url = `https://yuuka.cdn.diyigemt.com/image/ba-all-data/Audio/VoiceJp/Character_voice/${characterName}/${fileName}`;
  const sound = new Howl({
    src: url,
    html5: true,
    preload: true,
  })
    .on("loaderror", () => {
      playLoginVoice();
    })
    .on("load", () => {
      sound.play();
    });
}
